# 1. Написати програму, яка обчислюватиме суму, різницю і добуток двох цілих чисел.
def calcul(a,b):
    print(a, " + ", b, " = ", a+b )
    print(a, " - ", b, " = ", a-b )
    print(a, " * ", b, " = ", a*b )

a = int(input("введіть перше число => "))
b = int(input("введіть друге число => "))

calcul(a,b)